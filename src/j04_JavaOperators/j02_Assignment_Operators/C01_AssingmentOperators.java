package j04_JavaOperators.j02_Assignment_Operators;

public class C01_AssingmentOperators {
    public static void main(String[] args) {
        int hisAge = 29;
        System.out.println(hisAge + 5);
        System.out.println(hisAge);
        System.out.println(hisAge += 11);
        System.out.println(hisAge += 5);

    }
}
