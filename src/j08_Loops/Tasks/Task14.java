package j08_Loops.Tasks;

public class Task14 {
    //  Task->  0 ile 100 arasındaki bütün çift sayıları print eden code create ediniz.
    //Not: 0 ve 100 dahildir.

    public static void main(String[] args) {

        for (int i = 2; i <100 ; i+=2) {

               System.out.print(i + " ");

        }

    }
}
